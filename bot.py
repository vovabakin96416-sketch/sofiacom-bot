# ─── Кулдаун ─────────────────────────────────────────────────────────────────
COOLDOWN_HOURS = 0
cooldowns: dict[int, datetime] = {}   # user_id → время последнего ответа


def is_on_cooldown(user_id: int) -> bool:
    last = cooldowns.get(user_id)
    if last is None:
        return False
    return datetime.now() - last < timedelta(hours=COOLDOWN_HOURS)


