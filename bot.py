# ─── Обработчик сообщений ──────────────────────────────────────────────────────
async def handle_message(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    msg = update.message
    if not msg or not msg.text:
        return

    text = msg.text.strip()
    user = msg.from_user
    user_id = user.id
    name = f"@{user.username}" if user.username else user.first_name

    # Определяем, на какой триггер сработало
    if KARTA.match(text):
        pool = KARTA_POOL
    elif KOFE.match(text):
        pool = KOFE_POOL
    elif RUNA.match(text):
        pool = RUNA_POOL
    else:
        return  # не наш триггер — молчим

    # Кулдаун
    if is_on_cooldown(user_id):
        logger.info(f"Кулдаун для {name} ({user_id})")
        return

    # Выбираем случайный ответ и отвечаем
    response = random.choice(pool).replace("{name}", name)
    cooldowns[user_id] = datetime.now()

    await msg.reply_text(response)
    logger.info(f"Ответил {name} на '{text[:20]}'")


# ─── Запуск ──────────────────────────────────────────────────────────────────
def main() -> None:
    if BOT_TOKEN == "ВСТАВЬ_СВОЙ_ТОКЕН_ЗДЕСЬ":
        logger.error("Токен не задан! Укажи BOT_TOKEN в переменных окружения или прямо в коде.")
        return

    app = Application.builder().token(BOT_TOKEN).build()
    app.add_handler(MessageHandler(filters.TEXT & ~filters.COMMAND, handle_message))

    logger.info("Бот запущен. Жду сообщений...")
    app.run_polling(allowed_updates=Update.ALL_TYPES)


if __name__ == "__main__":
    main()
